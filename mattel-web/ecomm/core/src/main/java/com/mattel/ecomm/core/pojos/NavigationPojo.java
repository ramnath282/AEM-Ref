package com.mattel.ecomm.core.pojos;

/**
 * @author CTS
 *
 */
public class NavigationPojo {

	private String navLinkText;
	private String navLinkUrl;
	private String navLinkOpenIn;

	private String storeLocation;
	private String storeLink;

	/**
	 * @return the navLinkText
	 */
	public String getNavLinkText() {
		return navLinkText;
	}

	/**
	 * @param navLinkText
	 *            the navLinkText to set
	 */
	public void setNavLinkText(String navLinkText) {
		this.navLinkText = navLinkText;
	}

	/**
	 * @return the navLinkUrl
	 */
	public String getNavLinkUrl() {
		return navLinkUrl;
	}

	/**
	 * @param navLinkUrl
	 *            the navLinkUrl to set
	 */
	public void setNavLinkUrl(String navLinkUrl) {
		this.navLinkUrl = navLinkUrl;
	}

	/**
	 * @return the navLinkOpenIn
	 */
	public String getNavLinkOpenIn() {
		return navLinkOpenIn;
	}

	/**
	 * @param navLinkOpenIn
	 *            the navLinkOpenIn to set
	 */
	public void setNavLinkOpenIn(String navLinkOpenIn) {
		this.navLinkOpenIn = navLinkOpenIn;
	}

	/**
	 * @return the storeLocation
	 */
	public String getStoreLocation() {
		return storeLocation;
	}

	/**
	 * @param storeLocation
	 *            the storeLocation to set
	 */
	public void setStoreLocation(String storeLocation) {
		this.storeLocation = storeLocation;
	}

	/**
	 * @return the storeLink
	 */
	public String getStoreLink() {
		return storeLink;
	}

	/**
	 * @param storeLink
	 *            the storeLink to set
	 */
	public void setStoreLink(String storeLink) {
		this.storeLink = storeLink;
	}
}
