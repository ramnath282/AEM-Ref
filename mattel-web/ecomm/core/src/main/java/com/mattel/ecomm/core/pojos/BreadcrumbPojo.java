package com.mattel.ecomm.core.pojos;

public class BreadcrumbPojo {
	private String title;
	private String pageLink;
	private String adobeTrackingName;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPageLink() {
		return pageLink;
	}

	public void setPageLink(String pageLink) {
		this.pageLink = pageLink;
	}

	public String getAdobeTrackingName() {
		return adobeTrackingName;
	}

	public void setAdobeTrackingName(String adobeTrackingName) {
		this.adobeTrackingName = adobeTrackingName;
	}
}
