package com.mattel.ecomm.core.pojos;

public class ProductBadgePojo {

	private String badgeColour;

	private String badgeDisplayValue;

	private String badge;

	private String badgeIcon;

	private String textColour;

	public String getBadgeColour() {

		return badgeColour;
	}

	public void setBadgeColour(String badgeColour) {
		this.badgeColour = badgeColour;
	}

	public String getBadgeDisplayValue() {

		return badgeDisplayValue;

	}

	public void setBadgeDisplayValue(String badgeDisplayValue) {

		this.badgeDisplayValue = badgeDisplayValue;

	}

	public String getBadge() {

		return badge;

	}

	public void setBadge(String badge) {

		this.badge = badge;

	}

	public String getBadgeIcon() {

		return badgeIcon;

	}

	public void setBadgeIcon(String badgeIcon) {

		this.badgeIcon = badgeIcon;

	}

	public String getTextColour() {

		return textColour;

	}

	public void setTextColour(String textColour) {

		this.textColour = textColour;

	}

}