package com.mattel.play.core.pojos;
 public class CountryDropDownPojo {
	
	private String countryName;	
	private String countryUrl;	
	private String target;
	private String isCurrentCountry;
	
 	public String getCountryName() {
		return countryName;
	}
 	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
 	public String getCountryUrl() {
		return countryUrl;
	}
 	public void setCountryUrl(String countryUrl) {
		this.countryUrl = countryUrl;
	}
 	public String getTarget() {
		return target;
	}
 	public void setTarget(String target) {
		this.target = target;
	}
	public String getIsCurrentCountry() {
		return isCurrentCountry;
	}
	public void setIsCurrentCountry(String isCurrentCountry) {
		this.isCurrentCountry = isCurrentCountry;
	}
 	
 	
 }