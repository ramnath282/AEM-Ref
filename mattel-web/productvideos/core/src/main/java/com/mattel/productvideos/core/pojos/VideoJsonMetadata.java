package com.mattel.productvideos.core.pojos;

public class VideoJsonMetadata {
  private String scene7Path;
  private String brandValue;
  private String collectionName;
  private String contentName;
  
  public String getScene7Path() {
    return scene7Path;
  }

  public void setScene7Path(String scene7Path) {
    this.scene7Path = scene7Path;
  }

  public String getBrandValue() {
    return brandValue;
  }
  
  public void setBrandValue(String brandValue) {
    this.brandValue = brandValue;
  }

  public String getCollectionName() {
    return collectionName;
  }
  
  public void setCollectionName(String collectionName) {
    this.collectionName = collectionName;
  }
  
  public String getContentName() {
    return contentName;
  }
    
  public void setContentName(String contentName) {
    this.contentName = contentName;
  }
  
}
